package acsse.csc2a.fmb.model;

/**
 * Enum representing colors.
 * Each color is represented as a constant value.
 */
public enum E_COLOUR {
    /**
     * Red color constant.
     */
    RED,
    /**
     * Green color constant.
     */
    GREEN,
    /**
     * Blue color constant.
     */
    BLUE,
    /**
     * Yellow color constant.
     */
    YELLOW,
    /**
     * Magenta color constant.
     */
    MAGENTA,
    /**
     * White color constant.
     */
    WHITE,
    /**
     * Cyan color constant.
     */
    CYAN
}
